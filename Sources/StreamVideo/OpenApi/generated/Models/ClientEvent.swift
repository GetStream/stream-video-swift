import Foundation

public final class ClientEvent: @unchecked Sendable, Codable, JSONEncodable, Hashable {
    /// Call session ID associated with the attempt. Required on every event except CoordinatorJoin initiation and CoordinatorJoin failure (where the call session is not yet established); optional on MediaDevicePermission.
    public var callSessionId: String?
    /// Camera permission status: INITIATED, FAILED, GRANTED, or NOT_INITIATED. Required on every MediaDevicePermission event.
    public var cameraPermissionStatus: String?
    /// UUID generated by the client and shared across every event of the same coordinator connection. Required on every event except JoinInitiated, which is reported before a coordinator connection exists.
    public var coordinatorConnectId: String?
    /// Milliseconds elapsed between the stage attempt's initiation and this event. 
    public var elapsedTime: Int?
    /// Whether the event marks the start (initiated) or resolution (completed) of a stage attempt, or another event-specific value
    public var eventType: String?
    /// Terminal state of the peer connection. Required on PeerConnectionConnect failure.
    public var iceState: String?
    /// Call ID associated with the event. Required on every stage except CoordinatorWS, where it is optional.
    public var id: String?
    /// UUID generated by the client and shared across JoinInitiated and the join-lifecycle events (CoordinatorJoin, WSJoin, PeerConnectionConnect) of the same overall join attempt. Required on every join event except CoordinatorWS, which is reported before a join attempt is established.
    public var joinAttemptId: String?
    /// Microphone permission status: INITIATED, FAILED, GRANTED, or NOT_INITIATED. Required on every MediaDevicePermission event.
    public var microphonePermissionStatus: String?
    /// Resolution of a completed event: success or failure. Required on completed join events; forbidden on initiated join events.
    public var outcome: String?
    /// Which peer connection a PeerConnectionConnect event reports on: publish or subscribe. Required on every PeerConnectionConnect event.
    public var peerConnection: String?
    /// UTC timestamp at which the ICE connection was established earlier in the session, when applicable
    public var previouslyConnectedTimestamp: Date?
    /// Total in-stage retries the client made before resolving (0–1000). Required on completed join events.
    public var retryCountAttempt: Int?
    /// Failure code string. Required on CoordinatorJoin, CoordinatorWS, WSJoin, and PeerConnectionConnect failure.
    public var retryFailureCode: String?
    /// Failure reason string. Required on CoordinatorJoin, CoordinatorWS, WSJoin, and PeerConnectionConnect failure.
    public var retryFailureReason: String?
    /// Screen-share permission status: INITIATED, FAILED, GRANTED, or NOT_INITIATED. Optional on MediaDevicePermission events.
    public var screenShareStatus: String?
    /// Version of the client SDK
    public var sdkVersion: String?
    /// Identifier of the SFU the client was attempting to connect to. Required on WSJoin and PeerConnectionConnect failure, and on FirstAudioFrame and FirstVideoFrame.
    public var sfuId: String?
    /// Discriminator identifying the event kind. JoinInitiated marks the start of a join attempt; join-lifecycle events use CoordinatorJoin, CoordinatorWS, WSJoin, or PeerConnectionConnect; media-readiness events use FirstAudioFrame or FirstVideoFrame; MediaDevicePermission reports device permission results; other values denote generic client events.
    public var stage: String?
    /// UUID generated by the client at initiation. Identical on the matching completion event. Absent on JoinInitiated.
    public var stageId: String?
    /// UTC timestamp at which the event was recorded
    public var timestamp: Date?
    /// Identifier of the media track the frame belongs to. Required on FirstVideoFrame; optional on FirstAudioFrame.
    public var trackId: String?
    /// Call type associated with the event. Required on every stage except CoordinatorWS, where it is optional.
    public var type: String?
    /// User agent string of the client SDK
    public var userAgent: String?
    /// ID of the user the event was recorded for
    public var userId: String?
    /// Whether the ICE connection had been established earlier in the same session. Required on every PeerConnectionConnect event so reconnects can be distinguished from fresh connects.
    public var wasPreviouslyConnected: Bool?

    public init(callSessionId: String? = nil, cameraPermissionStatus: String? = nil, coordinatorConnectId: String? = nil, elapsedTime: Int? = nil, eventType: String? = nil, iceState: String? = nil, id: String? = nil, joinAttemptId: String? = nil, microphonePermissionStatus: String? = nil, outcome: String? = nil, peerConnection: String? = nil, previouslyConnectedTimestamp: Date? = nil, retryCountAttempt: Int? = nil, retryFailureCode: String? = nil, retryFailureReason: String? = nil, screenShareStatus: String? = nil, sdkVersion: String? = nil, sfuId: String? = nil, stage: String? = nil, stageId: String? = nil, timestamp: Date? = nil, trackId: String? = nil, userAgent: String? = nil, userId: String? = nil, wasPreviouslyConnected: Bool? = nil) {
        self.callSessionId = callSessionId
        self.cameraPermissionStatus = cameraPermissionStatus
        self.coordinatorConnectId = coordinatorConnectId
        self.elapsedTime = elapsedTime
        self.eventType = eventType
        self.iceState = iceState
        self.id = id
        self.joinAttemptId = joinAttemptId
        self.microphonePermissionStatus = microphonePermissionStatus
        self.outcome = outcome
        self.peerConnection = peerConnection
        self.previouslyConnectedTimestamp = previouslyConnectedTimestamp
        self.retryCountAttempt = retryCountAttempt
        self.retryFailureCode = retryFailureCode
        self.retryFailureReason = retryFailureReason
        self.screenShareStatus = screenShareStatus
        self.sdkVersion = sdkVersion
        self.sfuId = sfuId
        self.stage = stage
        self.stageId = stageId
        self.timestamp = timestamp
        self.trackId = trackId
        self.userAgent = userAgent
        self.userId = userId
        self.wasPreviouslyConnected = wasPreviouslyConnected
    }

    public enum CodingKeys: String, CodingKey, CaseIterable {
        case callSessionId = "call_session_id"
        case cameraPermissionStatus = "camera_permission_status"
        case coordinatorConnectId = "coordinator_connect_id"
        case elapsedTime = "elapsed_time"
        case eventType = "event_type"
        case iceState = "ice_state"
        case id
        case joinAttemptId = "join_attempt_id"
        case microphonePermissionStatus = "microphone_permission_status"
        case outcome
        case peerConnection = "peer_connection"
        case previouslyConnectedTimestamp = "previously_connected_timestamp"
        case retryCountAttempt = "retry_count_attempt"
        case retryFailureCode = "retry_failure_code"
        case retryFailureReason = "retry_failure_reason"
        case screenShareStatus = "screen_share_status"
        case sdkVersion = "sdk_version"
        case sfuId = "sfu_id"
        case stage
        case stageId = "stage_id"
        case timestamp
        case trackId = "track_id"
        case type
        case userAgent = "user_agent"
        case userId = "user_id"
        case wasPreviouslyConnected = "was_previously_connected"
    }

    public static func == (lhs: ClientEvent, rhs: ClientEvent) -> Bool {
        lhs.callSessionId == rhs.callSessionId &&
        lhs.cameraPermissionStatus == rhs.cameraPermissionStatus &&
        lhs.coordinatorConnectId == rhs.coordinatorConnectId &&
        lhs.elapsedTime == rhs.elapsedTime &&
        lhs.eventType == rhs.eventType &&
        lhs.iceState == rhs.iceState &&
        lhs.id == rhs.id &&
        lhs.joinAttemptId == rhs.joinAttemptId &&
        lhs.microphonePermissionStatus == rhs.microphonePermissionStatus &&
        lhs.outcome == rhs.outcome &&
        lhs.peerConnection == rhs.peerConnection &&
        lhs.previouslyConnectedTimestamp == rhs.previouslyConnectedTimestamp &&
        lhs.retryCountAttempt == rhs.retryCountAttempt &&
        lhs.retryFailureCode == rhs.retryFailureCode &&
        lhs.retryFailureReason == rhs.retryFailureReason &&
        lhs.screenShareStatus == rhs.screenShareStatus &&
        lhs.sdkVersion == rhs.sdkVersion &&
        lhs.sfuId == rhs.sfuId &&
        lhs.stage == rhs.stage &&
        lhs.stageId == rhs.stageId &&
        lhs.timestamp == rhs.timestamp &&
        lhs.trackId == rhs.trackId &&
        lhs.type == rhs.type &&
        lhs.userAgent == rhs.userAgent &&
        lhs.userId == rhs.userId &&
        lhs.wasPreviouslyConnected == rhs.wasPreviouslyConnected
    }

    public func hash(into hasher: inout Hasher) {
        hasher.combine(callSessionId)
        hasher.combine(cameraPermissionStatus)
        hasher.combine(coordinatorConnectId)
        hasher.combine(elapsedTime)
        hasher.combine(eventType)
        hasher.combine(iceState)
        hasher.combine(id)
        hasher.combine(joinAttemptId)
        hasher.combine(microphonePermissionStatus)
        hasher.combine(outcome)
        hasher.combine(peerConnection)
        hasher.combine(previouslyConnectedTimestamp)
        hasher.combine(retryCountAttempt)
        hasher.combine(retryFailureCode)
        hasher.combine(retryFailureReason)
        hasher.combine(screenShareStatus)
        hasher.combine(sdkVersion)
        hasher.combine(sfuId)
        hasher.combine(stage)
        hasher.combine(stageId)
        hasher.combine(timestamp)
        hasher.combine(trackId)
        hasher.combine(type)
        hasher.combine(userAgent)
        hasher.combine(userId)
        hasher.combine(wasPreviouslyConnected)
    }
}
